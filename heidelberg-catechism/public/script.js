function unhide() {
  const hid = document.getElementsByClassName("exp");
  if(hid[0].offsetWidth > 0 && hid[0].offsetHeight > 0) {
    hid[0].style.visibility = 'visible';
 }else{
  hid.style.visibility = 'none';
 }
}


